/* interesting way to describe event listeners: every time something happens, like a click or
a zoom change, the object being acted on sends an event. you can subscribe to this event with a
function - that is, you can set up notifications to listen for the event, and react in some way
when it happens! I understand a little better now. */

//event listener function, improved: generates a popup, rather than an alert
var popup = L.popup();

function onMapClick(e) {
    popup
        .setLatLng(e.latlng)
        .setContent("You clicked the map at " + e.latlng.toString())
        .openOn(mymap);
}

mymap.on('click', onMapClick);

